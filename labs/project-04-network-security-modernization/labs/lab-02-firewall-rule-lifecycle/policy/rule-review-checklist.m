# Firewall Rule Review Checklist

## Rule Metadata
- [ ] Business justification documented
- [ ] Owner assigned
- [ ] Expiration date defined

## Security Review
- [ ] Least privilege enforced
- [ ] Ports and protocols justified
- [ ] Logging enabled

## Risk Review
- [ ] Blast radius assessed
- [ ] Compensating controls documented if applicable
- [ ] Risk acceptance recorded if required

## Decision
- [ ] Approved
- [ ] Modified
- [ ] Removed
